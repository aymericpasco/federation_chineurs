     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store($teamId, $projectId, $todoId, Request $request)
    {
        $todo = Todo::whereId($todoId)->firstOrFail();

        $task = new Task(array(
           'name' => $request->get('name'),
        ));

        $task->todo()->associate($todo);
        $task->save();
    }

